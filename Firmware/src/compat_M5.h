// The M5Stack supports a complete graphics subsystem for the internal TFT display including free fonts,
// but no iconic font like in the great U8G library.
// For compatibility reasons the glyphs are converted to XBM and drawn with M5.Lcd.drawXBitmap() instead of
// using the whole "Open Iconic" font and drawGlyph().
//
// Font replacements:
//
// U8G: u8g2_font_inb16_mf                -> M5.Lcd: FreeSansBold12pt7b (FSB12)
// U8G: u8g2_font_inb16_mn                -> M5.Lcd: FreeMonoBold12pt7b (FMB12)
// U8G: u8g2_font_amstrad_cpc_extended_8f -> M5.Lcd: GLCD font #1

